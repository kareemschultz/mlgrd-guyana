#!/usr/bin/env python3
"""Generate D1 seed SQL for the generic `datasets` table.

Writes two files:
  scripts/d1-seed-datasets.sql            (COMMITTED)   — the 8 public datasets
  scripts/d1-seed-datasets-villages.sql   (GIT-IGNORED) — Amerindian villages
                                                          WITH personal contacts

Public datasets come from the committed, already-stripped JSON in
src/data/datasets/. The villages seed (which includes the sensitive
leader_name + contact) is generated from a LOCAL source only and must never be
committed — apply it directly to D1 with wrangler.

Usage:
  python scripts/gen-datasets-seed.py \
    [--villages "C:/Users/.../amerindian-villages.json"]
"""
import argparse
import json
import os
from datetime import datetime, timezone

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA_DIR = os.path.join(ROOT, "src", "data", "datasets")
NOW = datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")

# Public datasets (committed JSON). Villages handled separately (sensitive).
PUBLIC_KINDS = [
    "schools", "health-centres", "police-stations", "burial-grounds",
    "developments-2026", "resources", "staff",
]


def sql_str(value: str) -> str:
    return "'" + value.replace("'", "''") + "'"


def insert(kind: str, rows: list) -> list:
    out = []
    for r in rows:
        rid = str(r.get("id") or "")
        data = {k: v for k, v in r.items() if k != "id"}
        out.append(
            "INSERT OR REPLACE INTO datasets (id,kind,data,createdAt) VALUES ("
            f"{sql_str(rid)},{sql_str(kind)},{sql_str(json.dumps(data, ensure_ascii=False))},{sql_str(NOW)});"
        )
    return out


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--villages", help="Path to the FULL amerindian-villages.json (with leader_name + contact)")
    args = ap.parse_args()

    # Public seed (committed)
    lines = ["-- MLGRD datasets seed (PUBLIC). Generated by scripts/gen-datasets-seed.py.",
             "-- Apply: wrangler d1 execute mlgrd --remote --file=scripts/d1-seed-datasets.sql", ""]
    total = 0
    for kind in PUBLIC_KINDS:
        path = os.path.join(DATA_DIR, kind + ".json")
        rows = json.load(open(path, encoding="utf-8"))
        stmts = insert(kind, rows)
        total += len(stmts)
        lines.append(f"-- {kind}: {len(stmts)} rows")
        lines.extend(stmts)
        lines.append("")
    out_path = os.path.join(ROOT, "scripts", "d1-seed-datasets.sql")
    open(out_path, "w", encoding="utf-8").write("\n".join(lines))
    print(f"wrote {out_path} ({total} rows across {len(PUBLIC_KINDS)} datasets)")

    # Sensitive villages seed (git-ignored)
    src = args.villages
    if not src:
        guess = os.path.join(os.path.expanduser("~"), "Downloads", "_data_extract", "data", "amerindian-villages.json")
        src = guess if os.path.exists(guess) else None
    if src and os.path.exists(src):
        rows = json.load(open(src, encoding="utf-8"))
        stmts = insert("amerindian-villages", rows)
        v_lines = ["-- MLGRD Amerindian villages seed (SENSITIVE — personal contacts). GIT-IGNORED.",
                   "-- Apply: wrangler d1 execute mlgrd --remote --file=scripts/d1-seed-datasets-villages.sql", ""]
        v_lines.extend(stmts)
        v_path = os.path.join(ROOT, "scripts", "d1-seed-datasets-villages.sql")
        open(v_path, "w", encoding="utf-8").write("\n".join(v_lines))
        has_contact = sum(1 for r in rows if r.get("contact"))
        print(f"wrote {v_path} ({len(stmts)} villages, {has_contact} with personal contact) — GIT-IGNORED")
    else:
        print("NOTE: villages source not found; skipped sensitive seed. Pass --villages <path>.")


if __name__ == "__main__":
    main()
